function calculateGrade(result) {
  if (result >= 4.0) {
    console.log("A+");
  } else if (result >= 3.75) {
    console.log("A");
  } else if (result >= 3.5) {
    console.log("A-");
  } else if (result >= 3.25) {
    console.log("B+");
  } else if (result >= 3.0) {
    console.log("B");
  } else if (result >= 2.75) {
    console.log("B-");
  } else if (result >= 2.5) {
    console.log("C+");
  } else if (result >= 2.25) {
    console.log("C");
  } else if (result >= 2.0) {
    console.log("D");
  } else {
    console.log("F");
  }
}
